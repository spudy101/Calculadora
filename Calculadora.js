class Calculadora {
    sumar(num1, num2) {
        return num1 + num2;
    }

    restar(num1, num2) {
        return num1 - num2;
    }

    dividir(num1, num2) {
        return num1 / num2;
    }

    multiplicar(num1, num2) {
        return num1 * num2;
    }

    exponente(num1, num2) {
        return num1 ** num2;
    }

    sacarResto(num1, num2) {
        return num1 % num2;
    }
} 